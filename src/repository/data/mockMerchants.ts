const interpolate = (start: number, end: number, index: number): number => {
  const ratio = index / Math.max(metricDates.length - 1, 1);
  return start + (end - start) * ratio;
};

const createMerchant = (seed: MerchantSeed): Merchant => ({
  id: seed.id,
  name: seed.name,
  industry: seed.industry,
  status: seed.status,
  monthlyRevenue: seed.monthlyRevenue,
  lastActivityAt: seed.lastActivityAt,
  profile: {
    id: `${seed.id}-profile`,
    merchantId: seed.id,
    businessName: seed.name,
    industry: seed.industry,
    ownerName: seed.ownerName,
    ownerEmail: seed.ownerEmail,
    joinedAt: seed.joinedAt,
    lastContactedAt: seed.lastContactedAt,
  },
});

const createMetrics = (seed: MerchantSeed): DailyMetric[] =>
  metricDates.map((date, index) => ({
    merchantId: seed.id,
    date,
    transactionCount: Math.round(
      interpolate(seed.metricProfile.startTransactions, seed.metricProfile.endTransactions, index),
    ),
    grossRevenue: Math.round(
      interpolate(seed.metricProfile.startRevenue, seed.metricProfile.endRevenue, index),
    ),
    refundCount: Math.round(
      interpolate(seed.metricProfile.startTransactions, seed.metricProfile.endTransactions, index) *
        seed.metricProfile.refundRate,
    ),
    supportTicketCount: Math.max(
      0,
      Math.round(seed.metricProfile.supportTicketCount + (index % 2 === 0 ? 1 : -1)),
    ),
    loginCount: Math.max(0, Math.round(seed.metricProfile.loginCount + (index % 3) - 1)),
    featureUsageCount: Math.max(0, Math.round(seed.metricProfile.featureUsageCount + (index % 2))),
    refundRate: seed.metricProfile.refundRate,
  }));

export const seedDashboardData: DashboardData = {
  merchants: merchantSeeds.map(createMerchant),
  metrics: merchantSeeds.flatMap(createMetrics),
};
